		mz=nz;
		cudaFree(d_edge_point_state_);
        cudaFree(d_points_coor_);
        cudaFree(d_faces_index_);
        cudaFree(d_faces_ijkd_);
	    
        cudaMalloc((int**)&d_edge_point_state_,sizeof(int)*nx*ny*nz*3);
		d_set_int<<<GET_BLOCKS(nx*ny*nz*3),CUDA_NUM_THREADS>>>(nx*ny*nz*3, -1, d_edge_point_state_);
	    cudaMalloc((float**)&d_points_coor_,sizeof(float)*3*(int)(nx*ny*nz*12*0.05));
	    cudaMalloc((int**)&d_faces_index_,sizeof(long int)*3*(int)(nx*ny*nz*5*0.05));
	    cudaMalloc((int**)&d_faces_ijkd_,sizeof(int)*3*4*(int)(nx*ny*nz*5*0.05));
	}
	else
	{
		// std::vector<int> edge_point_state=std::vector<int>(nx*ny*nz*3,-1);
	 //    cudaMemcpy(d_edge_point_state_,edge_point_state.data(),sizeof(int)*nx*ny*nz*3,cudaMemcpyHostToDevice);
	    d_set_int<<<GET_BLOCKS(nx*ny*nz*3),CUDA_NUM_THREADS>>>(nx*ny*nz*3, -1, d_edge_point_state_);
	}
	NX=nx;
	NY=ny;
	NZ=nz;
	return true;
}
